<h1>About META</h1>
<h2><em>Slogan Here</em></h2>

<p>Inspired by 'Travel Assistance' threads on various forums, META has been developed to address a very simple problem... Vintage motorcycles break down at the worst possible time.</p>
<p>META allows vintage car and motorcycle enthusiasts to find help at a grassroots level when they're in need. The geolocation features of META make it easy for users to broadcast their location almost anywhere they have cell reception. The SMS features alert other nearby members who are willing to help.</p>
<p>I'd like to thank ChopCults staff and members for the inspiration to get this thing going.</p>

